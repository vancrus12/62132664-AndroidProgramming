package thigk2.lebavan.baithigk2;

public class DanhNhan {
    private String tenDanhNhan;
    private String anh;

    public DanhNhan(String tenDanhNhan, String anh) {
        this.tenDanhNhan = tenDanhNhan;
        this.anh = anh;
    }

    public String getTenDanhNhan() {
        return tenDanhNhan;
    }

    public void setTenDanhNhan(String tenDanhNhan) {
        this.tenDanhNhan = tenDanhNhan;
    }

    public String getAnh() {
        return anh;
    }

    public void setAnh(String anh) {
        this.anh = anh;
    }
}


